function runV2XMonitor() {
    playSound('scan');
    const terminal = document.getElementById('main-terminal');
    const status = document.getElementById('status-tag');
    const monitor = document.querySelector('.monarch-monitor');

    // бҹЎ. бһ”бҹ’бһҸбһјбһҡбһҹбҹ’бһҗбһ¶бһ“бһ—бһ¶бһ– UI бһ‘бҹ…бһҮбһ¶бһ–бһҺбҹҢбһҹбҹ’бһңбһ¶бһҷбһўбһ¶бһҳбҹҒбһ‘бһёбһҹ (Amethyst Purple) - бһҸбҹҶбһҺбһ¶бһ„бһұбҹ’бһҷ AI & V2X
    status.innerText = "бһҖбҹҶбһ–бһ»бһ„бһҹбҹ’бһҖбҹҒбһ“бһ”бһҺбҹ’бһҸбһ¶бһүбһ…бһҡбһ¶бһ…бһҡбһҺбҹҚбһўбһ“бҹ’бһҸбһҡбһ—бһ– V2X...";
    status.style.background = "#bf00ff";
    status.style.color = "#fff";
    status.style.boxShadow = "0 0 20px #bf00ff";

    if (monitor) {
        monitor.style.borderColor = "#bf00ff";
        monitor.style.boxShadow = "inset 0 0 60px rgba(191, 0, 255, 0.2)";
    }

    // бҹў. бһ‘бһ·бһ“бҹ’бһ“бһ“бҹҗбһҷбһ…бһҡбһ¶бһ…бһҡбһҺбҹҚбһҷбһ»бһ‘бҹ’бһ’бһҹбһ¶бһҹбҹ’бһҸбҹ’бһҡ
    const v2xLogs = [
        "> бһ–бҹ’бһҡбҹҮбһўбһ’бһ·бһҡбһ¶бһҮбһҹбһ¶бһҡбҹ– бһҖбҹҶбһ–бһ»бһ„бһ—бҹ’бһҮбһ¶бһ”бҹӢбһ‘бҹ…бһҖбһ¶бһ“бҹӢбһ”бҹ’бһҡбһ–бҹҗбһ“бҹ’бһ’ V2X Research Lab...",
        "> бһҹбҹ’бһҗбһ¶бһ“бһ—бһ¶бһ– V2V (Vehicle-to-Vehicle)бҹ– бһҷбһ¶бһ“бһҷбһ“бҹ’бһҸ бҹҘбҹ ,бҹ бҹ бҹ  бһӮбҹ’бһҡбһҝбһ„бһ”бһ¶бһ“бһҸбһ—бҹ’бһҮбһ¶бһ”бҹӢ...",
        "> бһҹбҹ’бһҗбһ¶бһ“бһ—бһ¶бһ– V2I (Vehicle-to-Infrastructure)бҹ– бһ•бҹ’бһӣбһјбһңбһўбһңбһҖбһ¶бһҹбһҹбҹ’бһҗбһ·бһҸбһҖбҹ’бһ“бһ»бһ„бһҹбҹ’бһҗбһ·бһҡбһ—бһ¶бһ–...",
        "> бһҖбһ¶бһҡбһңбһ·бһ—бһ¶бһӮбҹ– бһҖбһ¶бһҸбҹӢбһ”бһ“бҹ’бһҗбһҷбһҖбһ¶бһҡбһҖбһҖбһҹбҹ’бһ‘бҹҮ бһ“бһ·бһ„бһӮбҹ’бһҡбҹ„бҹҮбһҗбҹ’бһ“бһ¶бһҖбҹӢбһ…бһҡбһ¶бһ…бһҡбһҺбҹҚбһўбһ“бҹ’бһҸбһҡбһ—бһ–бһ”бһ¶бһ“ бҹ©бҹ©.бҹ©%...",
        "> бһӮбҹ„бһӣбһҠбҹ…бҹ– бһҸбһ—бҹ’бһҮбһ¶бһ”бҹӢбһ”бһҺбҹ’бһҸбһ¶бһүбһ‘бҹ…бһҖбһ¶бһ“бҹӢ https://tharahuokaing.github.io/V2X/ ...",
        "> бһ–бҹ’бһҡбҹҮбһўбһ’бһёбһҡбһ¶бһҮбһ”бһүбҹ’бһҮбһ¶бҹ– бһӮбҹ’бһҡбһ”бҹӢбһӮбҹ’бһҡбһ„бһӣбҹҶбһ бһјбһҡбһ…бһҡбһ¶бһ…бһҡбһҺбҹҚбһўбһ“бҹ’бһҸбһҡбһ—бһ– бһҖбҹ’бһҡбҹ„бһҳбһҖбҹ’бһҡбһҹбҹӮбһ—бҹ’бһ“бҹӮбһҖбһўбһ’бһ·бһҡбһ¶бһҮбһҹбһ¶бһҷбһ”бҹҗбһҡ!"
    ];

    terminal.innerHTML = "";
    
    v2xLogs.forEach((log, i) => {
        setTimeout(() => {
            const p = document.createElement('p');
            
            if (i === 1 || i === 4) {
                p.style.color = "#bf00ff";
                p.style.fontWeight = "bold";
            } else if (i === 5) {
                p.style.color = "#ffd700";
            } else {
                p.style.color = "#00f2ff";
            }

            p.innerHTML = log;
            terminal.appendChild(p);
            terminal.scrollTop = terminal.scrollHeight;

            if (i % 2 === 0) playSound('click');
        }, i * 800);
    });
}

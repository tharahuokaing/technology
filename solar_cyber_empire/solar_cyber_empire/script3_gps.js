/**
 * бһ–бҹ’бһҡбҹҮбһҡбһ¶бһҮбһ”бҹ’бһҡбһ–бҹҗбһ“бҹ’бһ’бһӮбҹ’бһҡбһ”бҹӢбһӮбҹ’бһҡбһ„бһҳбһ бһ¶бһҸбҹ’бһҡбһ”бһ¶бһүбҹӢбһҡбһ”бһ¶бҹҶбһ„бһҖбһ¶бһҡбһ–бһ¶бһҡбһ—бһ–бһҹбҹ…бһҡбҹҚ (Saturn Shield & Satellite Grid)
 * бһҹбҹ’бһҗбһ·бһҸбһҖбҹ’бһҡбҹ„бһҳбһ–бҹ’бһҡбҹҮбһҡбһ¶бһҮбһ бһҸбҹ’бһҗбһӣбҹҒбһҒбһ¶бһ”бһүбҹ’бһҮбһ¶бһҠбҹ„бһҷбһ•бҹ’бһ‘бһ¶бһӣбҹӢбҹ– бһ–бҹ’бһҡбҹҮбһҖбһҡбһ»бһҺбһ¶бһҮбһ¶бһўбһҳбҹ’бһ…бһ¶бһҹбҹӢбһҮбһёбһңбһ·бһҸбһӣбһҫбһҸбҹ’бһ”бһјбһ„ бһӣбҹ„бһҖбһҳбҹ’бһ…бһ¶бһҹбҹӢ бһ бһҪбһҖбһ¶бҹҶбһ„ бһҗбһ¶бһҡбҹүбһ¶
 */

function checkGpsRollover() {
    // бҹЎ. бһ”бһүбҹ’бһҮбһ¶бһҹбһјбһҡбһҹбһ–бҹ’бһ‘бһҹбҹ’бһҖбҹҒбһ“бһҳбһ бһ¶бһҹбҹ’бһ”бҹғбһҖбһ¶бһҡбһ–бһ¶бһҡбһҡбһ¶бһҮбҹ’бһҷ
    playSound('scan');

    const terminal = document.getElementById('main-terminal');
    const status = document.getElementById('status-tag');
    const monitor = document.querySelector('.monarch-monitor');

    // бҹў. бһ•бҹ’бһӣбһ¶бһҹбҹӢбһ”бҹ’бһҸбһјбһҡбһҡбһҹбҹ’бһҳбһёбһ“бҹғбһ•бҹ’бһ‘бһ¶бҹҶбһ„бһ”бһүбҹ’бһҮбһ¶ (UI) бһ‘бҹ…бһҮбһ¶бһ–бһҺбҹҢбһҳбһ„бҹ’бһӮбһӣбһҹбһ“бҹ’бһҸбһ·бһҹбһ»бһҒ (Imperial Security Green)
    status.innerText = "бһҖбҹҶбһ–бһ»бһ„бһҗбҹ’бһңбһ¶бһҷбһҖбһ¶бһҡбһҹбҹ’бһҖбҹҒбһ“бһҳбһ бһ¶бһҹбҹ’бһ”бҹғбһҖбһ¶бһҡбһ–бһ¶бһҡбһ—бһ–бһҹбҹ…бһҡбҹҚ (Saturn Shield)...";
    status.style.background = "#00ff88";
    status.style.color = "#000";
    status.style.boxShadow = "0 0 30px #00ff88, inset 0 0 10px #ffffff";

    // бҹЈ. бһ”бһ“бҹ’бһҗбҹӮбһҳбһҘбһ‘бҹ’бһ’бһ·бһ–бһӣбһ…бһҖбҹ’бһҒбһ» (Visual Effect) бһӣбһҫбһ–бҹ’бһҡбҹҮбһҡбһ¶бһҮбһҳбһҺбҹ’бһҢбһӣ (Shield Pulse Glow)
    if (monitor) {
        monitor.style.borderColor = "#00ff88";
        monitor.style.boxShadow = "inset 0 0 80px rgba(0, 255, 136, 0.4), 0 0 40px rgba(0, 255, 136, 0.2)";
    }

    // бҹӨ. бһҖбҹ’бһҡбһ¶бһ”бһ”бһ„бҹ’бһӮбҹҶбһ‘бһјбһӣбһҗбҹ’бһңбһ¶бһҷбһ‘бһ·бһ“бҹ’бһ“бһ“бҹҗбһҷбһ”бҹ’бһҡбһҸбһ·бһ”бһҸбҹ’бһҸбһ·бһҖбһ¶бһҡбһ•бҹ’бһҖбһ¶бһҷбһҡбһҺбһ”бһ“бҹғбһ–бҹ’бһҡбҹҮбһҡбһ¶бһҮбһўбһ¶бһҺбһ¶бһ…бһҖбҹ’бһҡ
    const shieldLogs = [
        "> бһ–бҹ’бһҡбҹҮбһҡбһ¶бһҮбһҹбһ¶бһҡбҹ– бһҖбҹҶбһ–бһ»бһ„бһҹбһҳбһҖбһ¶бһӣбһҖбһҳбҹ’бһҳбһ”бһҺбҹ’бһҸбһ¶бһүбһ•бҹ’бһҖбһ¶бһҷбһҡбһҺбһ”бһ”бһҡбһҳбһҒбҹ’бһҹбҹӮбһҖбҹ’бһҡбһңбһ¶бһҸбҹӢбһ—бһ–бһҹбҹ…бһҡбҹҚ...",
        "> бһҖбҹ’бһҡбһ¶бһ”бһ”бһ„бҹ’бһӮбҹҶбһ‘бһјбһӣбҹ– бһ•бҹ’бһҖбһ¶бһҷбһҡбһҺбһ”бһўбһ’бһ·бһҡбһ¶бһҮбһ…бҹҶбһ“бһҪбһ“ бҹЁбҹў бһӮбҹ’бһҡбһҝбһ„ бһҖбҹҶбһ–бһ»бһ„бһҹбҹ’бһҗбһ·бһҸбһҖбҹ’бһ“бһ»бһ„бһӮбһ“бҹ’бһӣбһ„бһ”бһ¶бһҡбһҳбһё...",
        "> бһ–бҹ’бһҡбҹҮбһҡбһ¶бһҮбһҳбһ бһ¶бһҹбҹ’бһ”бҹғ (Plasma Shield)бҹ– бһҠбҹҶбһҺбһҫбһҡбһҖбһ¶бһҡбһҹбҹ’бһҗбһ·бһҡбһ—бһ¶бһ–бһҖбһҳбҹ’бһҡбһ·бһҸбһҖбҹҶбһ–бһјбһӣ бҹ©бҹ©.бҹ©бҹ©%",
        "> бһҖбһ¶бһҡбһҸбҹ’бһҡбһҪбһҸбһ–бһ·бһ“бһ·бһҸбҹ’бһҷбһҡбһҹбҹ’бһҳбһёбһҡбһӣбһҖбһҹбһүбҹ’бһүбһ¶бҹ– бһӮбҹ’бһҳбһ¶бһ“бһҖбһ¶бһҡбһҮбҹ’бһҡбҹҖбһҸбһҮбҹ’бһҡбҹӮбһҖбһ–бһёбһўбһ¶бһҺбһ¶бһ…бһҖбҹ’бһҡбһ”бһ…бҹ’бһ…бһ¶бһҳбһ·бһҸбҹ’бһҡбһҒбһ¶бһ„бһҖбҹ’бһҡбҹ…...",
        "> бһ–бҹ’бһҡбҹҮбһҡбһ¶бһҮбһңбһ·бһ“бһ·бһ…бҹ’бһҶбҹҗбһҷбҹ– бһҠбҹӮбһ“бһўбһ¶бһҖбһ¶бһҹбһўбһ“бҹ’бһҸбһҡбһ—бһ–бһҹбҹ’бһҗбһ·бһҸбһҖбҹ’бһҡбҹ„бһҳбһҖбһ¶бһҡбһ”бһ·бһ‘бһҒбҹ’бһ‘бһ”бҹӢбһҠбһ¶бһ…бҹӢбһҒбһ¶бһҸбһҠбҹ„бһҷбһ–бҹ’бһҡбҹҮбһ”бһ¶бһҡбһҳбһё!",
        "> бһ–бҹ’бһҡбҹҮбһҡбһ¶бһҮбһұбһ„бҹ’бһҖбһ¶бһҡбһҠбһ¶бһ…бҹӢбһҒбһ¶бһҸбҹ– бһҡбһҖбҹ’бһҹбһ¶бһңбһ·бһ“бҹҗбһҷбһҖбһ¶бһҡбһ–бһ¶бһҡ бһ“бһ·бһ„бһҖбһҳбҹ’бһ‘бҹҒбһ…бһҡбһ¶бһӣбҹӢбһҷбһ¶бһ“бһҳбһ·бһ…бҹ’бһҶбһ¶бһ‘бһ·бһҠбҹ’бһӢбһ·бһұбҹ’бһҷбһўбһ“бҹ’бһҸбһҡбһ¶бһҷбһ—бҹ’бһӣбһ¶бһҳбҹ—!"
    ];

    // бҹҘ. бһ”бһ„бҹ’бһ бһ¶бһүбһ‘бһ·бһ“бҹ’бһ“бһ“бҹҗбһҷбһҗбҹ’бһңбһ¶бһҷбһӣбһҫбһ–бҹ’бһҡбҹҮбһҡбһ¶бһҮ Terminal
    terminal.innerHTML = ""; // бһӣбһ¶бһ„бһҹбһҳбҹ’бһўбһ¶бһҸбһҳбһҺбҹ’бһҢбһӣбһ‘бһ·бһ“бҹ’бһ“бһ“бҹҗбһҷ

    shieldLogs.forEach((log, i) => {
        setTimeout(() => {
            const p = document.createElement('p');

            // бһ”бҹ’бһҸбһјбһҡбһ–бһҺбҹҢбһўбһҸбҹ’бһҗбһ”бһ‘бһҸбһ¶бһҳбһӢбһ¶бһ“бҹҲбһ“бҹғбһҹбһ¶бһҡбһҹбһ“бҹ’бһҸбһ·бһҹбһ»бһҒбһҡбһ¶бһҮбҹ’бһҷ
            if (i === 2 || i === 4) {
                p.style.color = "#00ff88"; // бһ–бһҺбҹҢбһҳбһ„бҹ’бһӮбһӣбһҹбһ“бҹ’бһҸбһ·бһҹбһ»бһҒ
                p.style.textShadow = "0 0 10px rgba(0, 255, 136, 0.8)";
            } else if (i === 5) {
                p.style.color = "#ffd700"; // бһ–бһҺбҹҢбһҳбһ¶бһҹбһҹбһҳбҹ’бһҡбһ¶бһ”бҹӢбһ–бҹ’бһҡбҹҮбһҡбһ¶бһҮбһұбһ„бҹ’бһҖбһ¶бһҡбһҠбһ¶бһ…бҹӢбһҒбһ¶бһҸ
                p.style.fontWeight = "bold";
                p.style.fontSize = "1.1rem";
                p.style.textShadow = "0 0 20px rgba(255, 215, 0, 0.9)";
            } else {
                p.style.color = "#00f2ff"; // бһ–бһҺбҹҢбһҒбҹҖбһңбһҡбһҹбҹ’бһҳбһёбһҹбһ¶бһҷбһ”бҹҗбһҡ
            }

            p.innerHTML = log;
            terminal.appendChild(p);

            // бһҡбҹҶбһҖбһ·бһӣбһўбҹҒбһҖбҹ’бһҡбһ„бҹӢбһҹбҹ’бһңбҹҗбһҷбһ”бҹ’бһҡбһңбһҸбҹ’бһҸбһ·бһҗбҹ’бһңбһ¶бһҷбһҒбҹ’бһҹбҹӮбһ–бҹ’бһҡбҹҮбһ“бҹҒбһҸбҹ’бһҡ
            terminal.scrollTop = terminal.scrollHeight;

            // бһҹбһјбһҡбһҹбһ–бҹ’бһ‘бһ”бҹҠбһёбһ”бһҗбҹ’бһңбһ¶бһҷбһҠбҹҶбһҺбһ№бһ„бһ–бҹҒбһӣбһ–бҹ’бһҡбҹҮбһҡбһ¶бһҮбһҹбһ¶бһҡбһӣбҹҒбһ…бһЎбһҫбһ„
            if (i % 2 === 0) playSound('click');
        }, i * 800);
    });
}

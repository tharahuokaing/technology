        "> бһҖбһҳбҹ’бһҡбһ·бһҸбһҖбһҳбҹ’бһҸбҹ…бһ”бһҡбһҳбһҹбҹ’бһ“бһјбһӣбҹ– бһҹбҹ’бһҗбһ·бһҸбһҖбҹ’бһ“бһ»бһ„бһҡбһ„бҹ’бһңбһ„бҹӢбһҳбһ бһ·бһ‘бҹ’бһ’бһ·бһ«бһ‘бҹ’бһ’бһ· (Optimal Thermal Range)...",
        "> бһҠбҹҶбһҺбҹ„бҹҮбһҹбҹ’бһҡбһ¶бһҷбһҡбһ¶бһҮбҹ’бһҷбҹ– бһҖбҹҶбһ–бһ»бһ„бһ”бһ„бҹ’бһ бһјбһҡбһ‘бһ·бһ“бҹ’бһ“бһ“бҹҗбһҷбһӣбһҫбһҹбһ‘бҹ…бһҖбһ¶бһ“бҹӢбһғбҹ’бһӣбһ¶бҹҶбһ„бһ–бҹ’бһҡбҹҮбһҡбһ¶бһҮбһ‘бҹ’бһҡбһ–бҹ’бһҷбһҢбһёбһҮбһёбһҗбһӣбһ—бһ–бһ•бҹӮбһ“бһҠбһё...",

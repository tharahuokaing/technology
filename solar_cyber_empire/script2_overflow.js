    // бҹҘ. бһ”бһ„бҹ’бһ бһ¶бһүбһ‘бһ·бһ“бҹ’бһ“бһ“бҹҗбһҷбһӣбһҫ Terminal
    terminal.innerHTML = ""; // бһӣбһ¶бһ„бһҹбһҳбҹ’бһўбһ¶бһҸ Terminal
    
